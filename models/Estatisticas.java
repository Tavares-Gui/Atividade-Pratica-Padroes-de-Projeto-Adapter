package models;

public class Estatisticas {
    private int engajamento;
    private int visualizacoes;
    private int impressoes;
    private int plays;

    public int getEngajamento() { return engajamento; }
    public void setEngajamento(int engajamento) { this.engajamento = engajamento; }
    public int getVisualizacoes() { return visualizacoes; }
    public void setVisualizacoes(int visualizacoes) { this.visualizacoes = visualizacoes; }
    public int getImpressoes() { return impressoes; }
    public void setImpressoes(int impressoes) { this.impressoes = impressoes; }
    public int getPlays() { return plays; }
    public void setPlays(int plays) { this.plays = plays; }
}
